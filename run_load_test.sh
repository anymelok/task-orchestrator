#!/bin/bash

# Цвета для вывода в консоль
GREEN='\033[0;32m'
NC='\033[0m' # No Color

# Считываем аргументы или используем дефолтные значения (150 пользователей, спавн 10/сек)
USERS=${1:-150}
RATE=${2:-10}

echo -e "${GREEN}=== Запуск нагрузочного тестирования в Headless-режиме ===${NC}"
echo -e "Параметры: ${USERS} пользователей, скорость набора ${RATE}/сек, хост http://127.0.0.1:8000"
echo -e "Для остановки теста нажмите Ctrl+C"
echo -e "Grafana доступна по адресу: http://localhost:3000"
echo -e "========================================================="

# Запуск locust без веб-интерфейса
uv run locust -f locustfile.py \
  --headless \
  -u "${USERS}" \
  -r "${RATE}" \
  --host http://127.0.0.1:8000
